def sum_of_numbers_in_string(string):
    """
    Приймає рядок з числами, розділеними комами, і повертає їхню суму.
    Якщо в рядку є нечислові символи, повертає повідомлення про помилку.

    :param string: Рядок з числами, розділеними комами.
    :return: Сума чисел або повідомлення про помилку.
    """
    try:
        # Розділяємо рядок на елементи за комами
        numbers = string.split(',')
        # Перетворюємо кожен елемент у число і рахуємо їхню суму
        total = sum(int(num) for num in numbers)
        return total
    except ValueError:
        # Якщо є нечисловий символ, ловимо виняток і повертаємо повідомлення
        return "Не можу це зробити!"


# Приклад списку рядків
strings_list = ["1,2,3,4", "1,2,3,4,50", "qwerty1,2,3"]

# Опрацьовуємо кожен рядок списку і виводимо результат
for string in strings_list:
    print(sum_of_numbers_in_string(string))
